/**
 * DevTrack — Seed Projects for Load-Test Accounts
 * =================================================
 * Creates 1 project per load-test account directly in MongoDB.
 * Tasks endpoint requires a projectId — this ensures all 5 sessions
 * can exercise the write-heavy POST /project/dev/tasks/createtask/:id path.
 *
 * Run: node load-tests/seed-load-test-projects.js
 */

require('dotenv').config({ path: './config.env' });
const mongoose = require('mongoose');

const MONGO_URI = process.env.MONGO_URL || process.env.MONGO_URI || process.env.DATABASE;

if (!MONGO_URI) {
  console.error('❌  No MongoDB URI found. Check config.env');
  process.exit(1);
}

async function seed() {
  console.log('\n🔗  Connecting to MongoDB...');
  await mongoose.connect(MONGO_URI, { maxPoolSize: 5 });
  console.log('✅  Connected.\n');

  const developers = mongoose.connection.db.collection('developers');
  const projects   = mongoose.connection.db.collection('projects');

  const loadTestEmails = [
    'loadtest1@devtrack.io',
    'loadtest2@devtrack.io',
    'loadtest3@devtrack.io',
    'loadtest4@devtrack.io',
    'loadtest5@devtrack.io',
  ];

  // 1. Delete any existing load-test projects first to ensure clean seed
  console.log('🗑️  Deleting any existing [LoadTest] projects...');
  await projects.deleteMany({ name: { $regex: /^\[LoadTest\]/ } });

  let created = 0;

  for (const email of loadTestEmails) {
    const dev = await developers.findOne({ email });
    if (!dev) {
      console.warn(`⚠️   Developer not found: ${email} — run seed-load-test-accounts.js first`);
      continue;
    }

    // Reset projectCount back to 1
    await developers.updateOne(
      { _id: dev._id },
      { $set: { projectCount: 1 } }
    );

    // Build a minimal project document matching the project.schema.js
    const projectDoc = {
      name:        `[LoadTest] Project for ${email}`,
      description: 'Auto-generated by load-test seeder. Safe to delete.',
      status:      'active',
      isArchived:  false, // ← Crucial: Direct MongoDB insert bypasses mongoose default:false
      owner:       dev._id,
      members:     [],
      tasks:       [],
      budget:      0,
      spent:       0,
      createdAt:   new Date(),
      updatedAt:   new Date(),
    };

    const result = await projects.insertOne(projectDoc);
    console.log(`✅  Created project ${result.insertedId} for ${email}`);
    created++;
  }

  console.log(`\n📊  Project Seed Summary:`);
  console.log(`    ✅  Created : ${created}`);
  console.log(`\n🚀  Done. Re-run the k6 test — task creation will now be active.\n`);

  await mongoose.disconnect();
  console.log('🔌  Connection closed.');
}

seed().catch((err) => {
  console.error('\n❌  Project seeder failed:', err.message);
  mongoose.disconnect().finally(() => process.exit(1));
});
